def linear_function(x, a, b):
  # Calcula o resultado de uma equação da reta com coeficientes a e b para um dado valor de x
  # Parâmetros:
    # x: valor/variável independente
    # a: coeficiente angular da reta
    # b: coeficiente linear da reta
  # Retorna y = f(x) = a * x + b (int ou float)
  # Pode ser usado para traçar pontos de uma reta!
  return a * x + b